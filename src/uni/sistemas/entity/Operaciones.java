
package uni.sistemas.entity;
public class Operaciones {
    //atributos
    
    private double input;
    private double multi;
    private double div;
    private double restado;
    private double suma;
    private double n1;
    private double n2;
    
    //getter and setter

    public double getInput() {
        return input;
    }

    public void setInput(double input) {
        this.input = input;
    }

    public double getMulti() {
        return multi;
    }

    public void setMulti(double multi) {
        this.multi = multi;
    }

    public double getDiv() {
        return div;
    }

    public void setDiv(double div) {
        this.div = div;
    }

    public double getRestado() {
        return restado;
    }

    public void setRestado(double restado) {
        this.restado = restado;
    }

    public double getSuma() {
        return suma;
    }

    public void setSuma(double suma) {
        this.suma = suma;
    }

    public double getN1() {
        return n1;
    }

    public void setN1(double n1) {
        this.n1 = n1;
    }

    public double getN2() {
        return n2;
    }

    public void setN2(double n2) {
        this.n2 = n2;
    }
    
    
    
}
